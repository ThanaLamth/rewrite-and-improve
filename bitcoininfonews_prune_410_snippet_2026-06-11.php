function bitcoininfonews_prune_410_current_slug(): string
{
    $request_uri = $_SERVER['REQUEST_URI'] ?? '';
    $path = (string) wp_parse_url($request_uri, PHP_URL_PATH);
    $path = trim(rawurldecode($path), '/');

    return $path;
}

function bitcoininfonews_prune_410_matches_request(): bool
{
    $slug = bitcoininfonews_prune_410_current_slug();

    if ($slug === '') {
        return false;
    }

    return isset(bitcoininfonews_prune_410_slug_map()[$slug]);
}

add_filter('redirect_canonical', function ($redirect_url, $requested_url) {
    if (bitcoininfonews_prune_410_matches_request()) {
        return false;
    }

    return $redirect_url;
}, -9999, 2);

